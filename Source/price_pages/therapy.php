        <p class="directions__desc directions__desc--menu">
          Врач-терапевт — это первая инстанция, от которой в огромной степени зависит успех дальнейшего лечения, поэтому он должен быть очень компетентным и внимательным к нуждам пациента. А из-за дефицита времени врачи муниципальных поликлиник редко до конца выслушивают пациентов, а уж запоминают только постоянных визитеров.
        </p>
        <p class="directions__desc directions__desc--menu">
          Подумайте о себе: здоровье потерять легко, а восстанавливать его трудно, порой невозможно! Обратитесь к платному терапевту: в Уфе это сделать просто. Врач выслушает ваши жалобы, выдаст направление на необходимые исследования, тщательно изучит результаты. А потом либо сам назначит оптимальное лечение, либо направит к врачу соответствующей специализации.
        </p>
        <h2 class="doctors__title">Когда стоит обратиться к терапевту</h2>
        <p class="directions__desc directions__desc--menu">
          Это актуально при:
        </p>
        <ul class="list list--menu">
          <li class="list__item list__item--circle">повышенной температуре, лихорадочном состоянии;</li>
          <li class="list__item list__item--circle">головных болях;</li>
          <li class="list__item list__item--circle">кашле, чихании, болях в горле, заложенности носа;</li>
          <li class="list__item list__item--circle">расстройствах стула, тошноте, рвоте;</li>
          <li class="list__item list__item--circle">тяжести в животе, повышенном газообразовании, изжоге;</li>
          <li class="list__item list__item--circle">отсутствии или внезапном повышении аппетита;</li>
          <li class="list__item list__item--circle">резких колебаниях веса;</li>
          <li class="list__item list__item--circle">расстройствах мочеиспускания, крови в моче;</li>
          <li class="list__item list__item--circle">болях в области сердца;</li>
          <li class="list__item list__item--circle">одышке, учащенном сердцебиении;</li>
          <li class="list__item list__item--circle">повышенном или пониженном давлении, перепадах АД;</li>
          <li class="list__item list__item--circle">непроходящем чувстве усталости, угнетенности;</li>
          <li class="list__item list__item--circle">расстройствах сна;</li>
          <li class="list__item list__item--circle">постоянной раздражительности, тревожности;</li>
          <li class="list__item list__item--circle">появлении любых новообразований;</li>
          <li class="list__item list__item--circle">болях в животе, пояснице.</li>
        </ul>
        <p class="directions__desc directions__desc--menu">
          Не все эти проблемы может решить врач-терапевт, но он точно определит, как вам действовать дальше, чтобы вернуть утраченное здоровье!
        </p>
        <h2 class="doctors__title">Чем мы готовы помочь?</h2>
        <p class="directions__desc directions__desc--menu">
          Внимательно выслушать жалобы, собрать анамнез. При необходимости — оформить санаторно-курортную карту. Провести первичный осмотр, прослушать сердцебиение и дыхание, измерить давление и направить на диагностику. Провести простое моментальное тестирование на наличие Хеликобактер Пилори (возбудитель гастрита). Выдать направление на анализы, при необходимости — на УЗИ, МРТ, ЭКГ, рентгенографию. Разработать оптимальную тактику лечения либо направить на консультацию узкого специалиста. Хороший платный терапевт — это половина успеха лечения.
        </p>
        <h2 class="doctors__title">Преимущества платного терапевта<br> хозрасчетной поликлиники</h2>
