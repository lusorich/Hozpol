        <h2 class="doctors__title">Симптомы, при которых стоит показаться врачу</h2>
        <p class="directions__desc directions__desc--menu">
          Расстройства эндокринной системы могут проявляться такими симптомами:
        </p>
        <ul class="list list--menu">
          <li class="list__item list__item--circle">беспричинная усталость, апатия, депрессивные настроения, безразличие к жизни или наоборот, частая смена настроений;</li>
          <li class="list__item list__item--circle">резкое похудение или беспричинный набор веса;</li>
          <li class="list__item list__item--circle">дрожь в конечностях;</li>
          <li class="list__item list__item--circle">чрезмерная потливость;</li>
          <li class="list__item list__item--circle">сильное выпадение волос или ухудшение их внешнего вида;</li>
          <li class="list__item list__item--circle">непреходящая жажда или ощущение постоянной сухости во рту;</li>
          <li class="list__item list__item--circle">беспричинная тахикардия (частое сердцебиение);</li>
          <li class="list__item list__item--circle">накатывающие приливы жара;</li>
          <li class="list__item list__item--circle">ухудшение памяти, снижение интеллектуальных способностей.</li>
        </ul>
        <p class="directions__desc directions__desc--menu">При любых перечисленных выше симптомах незамедлительно приходите на приём к эндокринологу в нашу хозрасчетную поликлинику на Юношеской библиотеке, чтобы пройти обследование и, при необходимости, начать соответствующее лечение как можно раньше.
